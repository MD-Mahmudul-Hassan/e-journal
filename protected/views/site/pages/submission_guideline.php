    <div class="row-fluid"><h4>Format and Reference Style:</h4></div><hr>
    <div class="row-fluid" style="font-size: 17px;line-height: 30px;">
        •	Manuscripts should preferably be between 5,000 and 8,000 words and should be type written and double-spaced with at least 1 inch margin on all sides.<br>
•	British Spelling is uniformly accepted.<br>
•	Footnotes should be collated at the end of each page.<br> 
•	Footnotes to author(s)' names should be designated as *, ** etc. The asterisked footnote should give the author's position, institutional address and any brief acknowledgement if required.<br>
•	Footnotes to the text should be designated as 1, 2, 3 etc. <br>
•	Reference in Footnotes [For Example: for articles, author's name in natural order, "title of article" Journal, Vol., No., page; for books, author, title of the book (place, publisher) year, page] should be placed at the end.<br>
•	A brief cover letter indicating author’s present position, degrees, recent publications and current address should be supplied on a separate page. The cover should also involve a statement that the material is not under consideration elsewhere, and that it has not been published or is not pending publication elsewhere.<br>

